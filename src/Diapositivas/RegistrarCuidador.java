    private void jButtonRegistrarCuidadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarCuidadorActionPerformed

        // declaración variables
        int status, avanzar = 0;
        String nombre, apellido, usuario, contraseña, statusNivel = null;
        
        // obtener valores
        nombre = textNombreEmpleado.getText().trim();
        apellido = textApellido.getText().trim();
        usuario = textUsuario.getText().trim();
        contraseña = textContraseña.getText().trim();
        status = cmbStatus.getSelectedIndex() + 1;
        
         // verificación de que no haya campos sin completador.
        if(nombre.equals("")){
            textNombreEmpleado.setBackground(Color.red);
            avanzar++;
        }if(apellido.equals("")){
            textApellido.setBackground(Color.red);
            avanzar++;
        }if(usuario.equals("")){
            textUsuario.setBackground(Color.red);
            avanzar++;
        }if(contraseña.equals("")){
            textContraseña.setBackground(Color.red);
            avanzar++;
        }
        
        // dar valor al cmb de status
        if(status == 1){
            statusNivel = "Activo";
        }else if (status == 2){
            statusNivel = "Inactivo";
        }
        
        // cargar datos en la base de datos
        if(avanzar == 0){
        
            try{
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("insert into personalRefugio values (?, ?, ?, ?, ?, ?)");
               
                    pst.setInt(1, 0);
                    pst.setString(2, nombre);
                    pst.setString(3, apellido);
                    pst.setString(4, usuario);
                    pst.setString(5, contraseña);
                    pst.setString(6, statusNivel);
                    
                    pst.executeUpdate();
                    cn.close();
           
                    // vacío de campos
                    VaciarCampos();
                    
                    JOptionPane.showMessageDialog(null, "Has registrado un cuidador");
                    
            }catch(SQLException e){
                System.out.println("No se ha podido registrar al cuidador " + e);
                JOptionPane.showMessageDialog(null, "No se ha podido registrar al cuidador, "
                        + "por favor, contactese con el programador para solucionar"
                        + "el problema a la brevedad");
            }
        
        }else{    
            JOptionPane.showMessageDialog(null, "Debes completar todos los campos");
        }
        
    }//GEN-LAST:event_jButtonRegistrarCuidadorActionPerformed

