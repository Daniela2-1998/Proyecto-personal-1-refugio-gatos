    // datos de la tabla
    public void cargarDatos(){
        
        DefaultTableModel modelo = new DefaultTableModel();

        try{
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select NombreGato, IDGato, "
                    + "NumeroControl, Estado, Gastos from control");
            ResultSet rs = pst.executeQuery();
            
            jTableControles = new JTable(modelo);
            jScrollPane1.setViewportView(jTableControles);
            
            modelo.addColumn("NombreGato");
            modelo.addColumn("IDGato");
            modelo.addColumn("NumeroControl");
            modelo.addColumn("Estado");
            modelo.addColumn("Gastos");
            
            while(rs.next()){
                Object[] fila = new Object[5];
                for(int i = 0; i < 5; i++){
                    fila[i] = rs.getObject(i + 1);
                }
                modelo.addRow(fila);
            }
            cn.close();
            
        }catch(SQLException e){
            System.err.println("No se pudo cargar la data " + e);
            JOptionPane.showMessageDialog(null, "No se pudo mostrar la "
                    + "información en la tabla de controles");
        }
    }



