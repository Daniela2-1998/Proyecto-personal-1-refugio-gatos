    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
       
        DefaultTableModel modelo = new DefaultTableModel();
        
        String buscar;
        
        buscar = textBuscar.getText().trim();
        
        try{
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("select NombreGato, "
                    + "IDGato, NumeroControl, Estado, Gastos from control where "
                    + "NombreGato ='" + buscar + "' or NumeroControl = '" + buscar + "'" );
            ResultSet rs = pst.executeQuery();
            
            jTableControles = new JTable(modelo);
            jScrollPane1.setViewportView(jTableControles);
            jScrollPane1.setWheelScrollingEnabled(true);
            
           modelo.addColumn("NombreGato");
           modelo.addColumn("IDGato");
           modelo.addColumn("NumeroControl");
           modelo.addColumn("Estado");
           modelo.addColumn("Gastos");
 
           while(rs.next()){
               Object[] fila = new Object[5];
               for(int i = 0; i < 5; i++){
                   fila[i] = rs.getObject(i + 1);
               }
               modelo.addRow(fila);
           }
           cn.close();
        }catch(SQLException e){
            System.err.println("Error en la busqueda " + e);
            JOptionPane.showMessageDialog(null, "No se pudo buscar el valor");
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

